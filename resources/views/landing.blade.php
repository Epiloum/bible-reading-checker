      // lazy load
      function lazy() {
        $(function() {
         let $lz = $(".lazy");
         $lz.css({ "min-height": "500px" });

         $(window).scroll(function() {
           let $winScroll = $(this).scrollTop(),
            $windowHeight = $(window).height();

           $lz.each(function() {
            let $tg = $(this);

            if ($winScroll <= $tg.offset().top && $winScroll + $windowHeight >= $tg.offset().top - $windowHeight /4) {
              if ($tg.attr("loaded") != "true") {
               let $src = $tg.attr("data-src");
               $tg.attr({ src: $src });
               $tg.css({ "min-height": "" });
               $tg.removeAttr("data-src");
               $tg.attr({ loaded: "true" });
              }
            }
           });
         });
        });
      }
      
